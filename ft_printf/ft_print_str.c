#include "ft_printf.h"

int	ft_print_string(char *str)
{
	int	i;

	i = 0;
	if (!str)
		str = "(null)";
	while (str[i])
	{
		if (ft_print_char(str[i]) == -1)
			return (-1);
		i++;
	}
	return (i);
}
